void Render()
{
	for (int i = 0; i < HEIGHT;i++)
	{
		for (int j = 0; j < WIDTH;j++)
		{

			if (maze[i][j] == '0')
			{
				printf(" ");
			}
			else if (maze[i][j] == '1')
			{
				printf("■");
			}
		}
		printf("\n");
	}
}


